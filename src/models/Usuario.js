class Usuario{
    constructor(id, nome, email, senha, telefone, documento, data_nascimento, cidade){
        this.id = id;
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.documento = documento;
        this.data_nascimento = data_nascimento;
        this.cidade = cidade;
        this.senha = senha;
    }
}